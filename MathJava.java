public class MathJava {
    public static void main(String[] args){        

        System.out.println(Math.E);
        System.out.println(Math.PI);
        System.out.println(Math.abs(-12)); //absolute value of a number
        System.out.println(Math.acos(-0.3)); 
        System.out.println(Math.addExact(2, 3));
        
        System.out.println(Math.cbrt(9)); //cube root
        System.out.println(Math.ceil(2.3)); //ceiling value of a number
        System.out.println(Math.copySign(4, -2)); //takes sign of second number and puts it to the first number
        System.out.println(Math.cos(0.5)); 
        
        System.out.println(Math.decrementExact(9));
        
        System.out.println(Math.exp(1));
        System.out.println(Math.expm1(1)); 
        
        System.out.println(Math.floor(6.99));
        System.out.println(Math.floorDiv(5, 2));
        System.out.println(Math.floorMod(5, 2));
        
        System.out.println(Math.getExponent(64));

        System.out.println(Math.hypot(3, 4));
        
        System.out.println(Math.IEEEremainder(5.4, 3.1));
        System.out.println(Math.incrementExact(8));
        
        System.out.println(Math.log(Math.E));
        System.out.println(Math.log10(12));
        
        System.out.println(Math.max(98, 22));
        System.out.println(Math.min(3, 5));
        System.out.println(Math.multiplyExact(2, 6));
        

        System.out.println(Math.nextAfter(31.432, -5.4));
        System.out.println(Math.nextUp(53));
        
        System.out.println(Math.pow(3, 4));
        
        System.out.println(Math.random());
        
        System.out.println(Math.rint(4.3));
        System.out.println(Math.round(7));
        
        System.out.println(Math.scalb(2, 1));
        System.out.println(Math.signum(-90));
        System.out.println(Math.sin(-.2));
        System.out.println(Math.sinh(-0.2));
        System.out.println(Math.sqrt(4));
        System.out.println(Math.subtractExact(4, 1));
        
        System.out.println(Math.tan(-4.2));
        System.out.println(Math.tanh(4.5));
        System.out.println(Math.toDegrees(6.5));
        System.out.println(Math.toIntExact((long) 6.7));
        System.out.println(Math.toRadians(90));
        
        System.out.println(Math.ulp(6.444));
    }
    
}
